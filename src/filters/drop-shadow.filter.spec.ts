import { expect } from '@esm-bundle/chai';
import { prepareTestBed, imageSample } from '../utils/test.utils';

describe('filters/drop-shadow', () => {
  it('should apply the filter properly', () => {
    const context = prepareTestBed({ filters: ['drop-shadow(3px 3px 5px red)'], color: 0x284 });
    const image = context.getImageData(0, 0, 6, 6).data;
    expect(`${image}`).to.equal(
      imageSample(`
        255,000,000,002, 255,000,000,002, 255,000,000,003, 255,000,000,003, 255,000,000,003, 255,000,000,003,
        255,000,000,002, 255,000,000,003, 255,000,000,003, 255,000,000,003, 255,000,000,003, 255,000,000,004,
        255,000,000,003, 255,000,000,003, 034,136,068,255, 034,136,068,255, 255,000,000,004, 255,000,000,004,
        255,000,000,003, 255,000,000,003, 034,136,068,255, 034,136,068,255, 255,000,000,005, 255,000,000,005,
        255,000,000,003, 255,000,000,004, 255,000,000,004, 255,000,000,005, 255,000,000,005, 255,000,000,005,
        255,000,000,004, 255,000,000,004, 255,000,000,004, 255,000,000,005, 255,000,000,005, 255,000,000,005,
      `),
    );
  });
});
